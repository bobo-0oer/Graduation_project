package com.graduationproject.entity;

public class Comfortable {

    private String username;
    private String max_temperature;
    private String min_temperature;
    private String max_humidity;
    private String min_humidity;
    private String smokescope;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getMax_temperature() {
        return max_temperature;
    }

    public void setMax_temperature(String max_temperature) {
        this.max_temperature = max_temperature;
    }

    public String getMin_temperature() {
        return min_temperature;
    }

    public void setMin_temperature(String min_temperature) {
        this.min_temperature = min_temperature;
    }

    public String getMax_humidity() {
        return max_humidity;
    }

    public void setMax_humidity(String max_humidity) {
        this.max_humidity = max_humidity;
    }

    public String getMin_humidity() {
        return min_humidity;
    }

    public void setMin_humidity(String min_humidity) {
        this.min_humidity = min_humidity;
    }

    public String getSmokescope() {
        return smokescope;
    }

    public void setSmokescope(String smokescope) {
        this.smokescope = smokescope;
    }
}
